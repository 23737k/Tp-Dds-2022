package dds.grupo3.clases;

public class FactorDeEmision {

	TipoDeActividad tipoAct;
    TipoDeConsumo tipoCons;
    Float valor;
    Unidad unidad;
    
	
	public TipoDeActividad getTipoAct() {
		return tipoAct;
	}
	public void setTipoAct(TipoDeActividad tipoAct) {
		this.tipoAct = tipoAct;
	}
	public TipoDeConsumo getTipoCons() {
		return tipoCons;
	}
	public void setTipoCons(TipoDeConsumo tipoCons) {
		this.tipoCons = tipoCons;
	}
	public Float getValor() {
		return valor;
	}
	public void setValor(Float valor) {
		this.valor = valor;
	}
	public Unidad getUnidad() {
		return unidad;
	}
	public void setUnidad(Unidad unidad) {
		this.unidad = unidad;
	}
	    
	
	
}
